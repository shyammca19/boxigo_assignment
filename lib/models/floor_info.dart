class FloorInfo {
  const FloorInfo({
    required this.oldFloorNo,
    required this.newFloorNo,
    required this.oldElevator,
    required this.newElevator,
    required this.oldParking,
    required this.newParking,
    required this.packing,
    required this.unpacking,
  });

  final String oldFloorNo;
  final String newFloorNo;
  final String oldElevator;
  final String newElevator;
  final String oldParking;
  final String newParking;
  final String packing;
  final String unpacking;
}
